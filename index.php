    <h1>XXV FERIA DE UNIVERSIDADES IE COLEGIO SAN JOSE DE GUANENTA SAN GIL</h1>

    <p>La feria de Universidades IE Colegio San José de Guanentá San Gil, es un espacio
importante creado desde la academia para la orientación vocacional y profesional de los
estudiantes del grado 11 de la región Guanentina y comunera. La XXV Feria de Universidades IE
Colegio San José de Guanentá a realizarse los días <strong>25 y 26 de Septiembre de 2025 en San Gil</strong>, es un evento que contará con la presencia de las Instituciones educativas de orden superior, media y
básica tanto de carácter público como privado del país, cuyo fin principal es generar un espacio
dinámico e integral de socialización de los diferentes programas académicos de educación
superior, técnica y tecnológica ofertados por las instituciones educativas asistentes al evento.</p>
    <br>


    
    <h2>Objetivos de la Feria</h2>
    <p>• Ofrecer un espacio de encuentro entre instituciones educativas de carácter superior,
entidades financieras y las instituciones educativas de la región, del Departamento, del
resto del país, con los estudiantes de 11° grado, bachilleres, técnicos, tecnólogos,
profesionales, docentes, rectores, padres de familia y todas las personas con deseos de
superación en el ámbito académico.
    <br>
    <br>
    •Permitir la concentración de los estudiantes, en lugares específicos, con el
    propósito de recibir información actualizada y completa de cada institución participante.
    <br>
    <br>
    •Propiciar la orientación vocacional a la juventud interesada en ingresar a la educación
    superior, técnica o tecnológica.
    <br>
    <br>
    •Facilitar la creación, fortalecimiento y articulación de redes académicas que le permitan
    a las instituciones ofrecer una orientación vocacional y profesional para los bachilleres

    </p>
    <br>

